package com.beans;

public class Staff {
	private String Staff_id;
	private String S_name;
	private String Staff_add;
	private int S_SSN;
	private String S_role;
	private int Salary;
	private int Rating;
	private String User_id;
	public String getStaff_id() {
		return Staff_id;
	}
	public void setStaff_id(String staff_id) {
		Staff_id = staff_id;
	}
	public String getS_name() {
		return S_name;
	}
	public void setS_name(String s_name) {
		S_name = s_name;
	}
	public String getStaff_add() {
		return Staff_add;
	}
	public void setStaff_add(String staff_add) {
		Staff_add = staff_add;
	}

	public int getS_SSN() {
		return S_SSN;
	}
	public void setS_SSN(int s_SSN) {
		S_SSN = s_SSN;
	}
	public String getS_role() {
		return S_role;
	}
	public void setS_role(String s_role) {
		S_role = s_role;
	}

	public int getSalary() {
		return Salary;
	}
	public void setSalary(int salary) {
		Salary = salary;
	}
	public int getRating() {
		return Rating;
	}
	public void setRating(int rating) {
		Rating = rating;
	}
	public String getUser_id() {
		return User_id;
	}
	public void setUser_id(String user_id) {
		User_id = user_id;
	}
	
	
}
